package models;

public class DataTableExample {
    private String userName;
    private String passWord;
    private String textAreaComment;
    private String fileName;
    private String checkboxItems;
    private String radioItems;
    private String multipleSelectValues;
    private String dropDown;

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getPassWord() {
        return passWord;
    }

    public void setPassWord(String passWord) {
        this.passWord = passWord;
    }

    public String getTextAreaComment() {
        return textAreaComment;
    }

    public void setTextAreaComment(String textAreaComment) {
        this.textAreaComment = textAreaComment;
    }

    public String getFileName() {
        return fileName;
    }

    public void setFileName(String fileName) {
        this.fileName = fileName;
    }

    public String getCheckboxItems() {
        return checkboxItems;
    }

    public void setCheckboxItems(String checkboxItems) {
        this.checkboxItems = checkboxItems;
    }

    public String getRadioItems() {
        return radioItems;
    }

    public void setRadioItems(String radioItems) {
        this.radioItems = radioItems;
    }

    public String getMultipleSelectValues() {
        return multipleSelectValues;
    }

    public void setMultipleSelectValues(String multipleSelectValues) {
        this.multipleSelectValues = multipleSelectValues;
    }

    public String getDropDown() {
        return dropDown;
    }

    public void setDropDown(String dropDown) {
        this.dropDown = dropDown;
    }
}
